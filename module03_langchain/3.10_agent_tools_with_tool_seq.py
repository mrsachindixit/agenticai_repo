agent = create_agent(
    model=model,
    tools=[get_horoscope, get_user_details, update_horoscope_for_user],
    # debug = True
)

messages = [
    {"role":"system", "content": "You are a personal assistant. " 
    "When the user provides their user ID Do you will fetch their details using get_user_details tool, get their astrological sign returned by get_user_details tool, fetch their horoscope for the day using get_horoscope tool, and update the horoscope fetched by get_horoscope tool in the database using update_horoscope_for_user tool. " 
    "Use the provided tools to perform these actions. Always respond with the final horoscope update confirmation."},
    {"role": "user", "content": "My user ID is 123."}
]

ai_msg = agent.invoke({"messages": messages})

for i in range(len(ai_msg.get("messages"))):
    print(ai_msg.get('messages')[i])
